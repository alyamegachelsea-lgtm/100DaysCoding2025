public class Day020java{
  public static void main(String[] args){
  
    //tipe data primitif
       int a = 2365;
       double b = 35.0; 
       
    //cara membuat primitif ke string
       String c = Integer.toString(a);
       String d = Double.toString(b);
    
    //cetak
       System.out.println("Integer to String : "+ c);
       System.out.println("Double to String : "+ d);
  }
}
